import math

def solve(bars):
    len_bars = len(bars)
    max_area = -math.inf
    for i in range(len_bars-1):

        for j in range(i+1, len_bars):
            h = min(bars[i], bars[j])
            b = 3 * (j-i)
            area = h * b
            if area > max_area:
                max_area = area
    
    return max_area

